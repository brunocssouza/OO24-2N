// Declare duas variáveis numéricas: num1 e num2. 
// Calcule a soma, a diferença, o produto e o quociente de num1 e num2. 
// Imprima os resultados no console.

let num1:number = 2;
let num2:number = 3;

console.log(`
    Num1: ${num1}
    Num2: ${num2}

    ${num1} + ${num2} = ${num1+num2}
    ${num1} - ${num2} = ${num1-num2}
    ${num1} * ${num2} = ${num1*num2}
    ${num1} / ${num2} = ${num1/num2}
`);